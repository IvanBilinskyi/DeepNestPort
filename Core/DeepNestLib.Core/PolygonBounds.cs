namespace DeepNestLib
{
    public class PolygonBounds
    {
        public double x;
        public double y;
        public double width;
        public double height;
        public PolygonBounds(double _x, double _y, double _w, double _h)
        {
            x = _x;
            y = _y;
            width = _w;
            height = _h;
        }
    }
}
